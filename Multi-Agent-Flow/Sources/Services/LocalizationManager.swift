enum AppLanguage: String, CaseIterable, Identifiable {
    case simplifiedChinese = "zh-Hans"
    case traditionalChinese = "zh-Hant"
    case english = "en"
    
    var id: String { rawValue }
    
    var displayName: String {
        switch self {
        case .simplifiedChinese: return "简体中文"
        case .traditionalChinese: return "繁體中文"
        case .english: return "English"
        }
    }

    var shortDisplayName: String {
        switch self {
        case .simplifiedChinese: return "简体"
        case .traditionalChinese: return "繁中"
        case .english: return "EN"
        }
    }

    static func resolvedInitialLanguage() -> AppLanguage {
        if let preferred = Locale.preferredLanguages.first,
           let resolved = from(localeIdentifier: preferred) {
            return resolved
        }
        return .english
    }

    static func from(localeIdentifier: String) -> AppLanguage? {
        let normalized = localeIdentifier.lowercased()
        if normalized.hasPrefix("zh-hant") || normalized.hasPrefix("zh-tw") || normalized.hasPrefix("zh-hk") {
            return .traditionalChinese
        }
        if normalized.hasPrefix("zh") {
            return .simplifiedChinese
        }
        if normalized.hasPrefix("en") {
            return .english
        }
        return nil
    }
}

class LocalizationManager: ObservableObject {
    static let shared = LocalizationManager()
    
    @Published var currentLanguage: AppLanguage = .simplifiedChinese {
        didSet {
            UserDefaults.standard.set(currentLanguage.rawValue, forKey: "appLanguage")
            objectWillChange.send()
        }
    }
    
    private init() {
        if let savedLanguage = UserDefaults.standard.string(forKey: "appLanguage"),
           let language = AppLanguage(rawValue: savedLanguage) {
            currentLanguage = language
        } else {
            currentLanguage = AppLanguage.resolvedInitialLanguage()
        }
    }
    
    func setLanguage(_ language: AppLanguage) {
        currentLanguage = language
    }
}

